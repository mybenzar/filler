#include "libft.h"

void	*ft_memcpy(void *dest, void const *src, size_t n)
{
	char		*p_dest;
	char const	*p_src;
	size_t		i;

	p_dest = (char *)dest;
	p_src = (char const *)src;
	i = 0;
	while (i < n)
	{
		p_dest[i] = p_src[i];
		i++;
	}
	return (dest);
}
